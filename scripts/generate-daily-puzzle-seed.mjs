/**
 * Generates supabase/migrations/20250526000011_daily_puzzle_seed.sql
 * from src/data/dailySongs.js, dailyMembers.js, dailyLyrics.js
 *
 * Run: node scripts/generate-daily-puzzle-seed.mjs
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'
import { dailySongs } from '../src/data/dailySongs.js'
import { dailyMembers } from '../src/data/dailyMembers.js'
import { dailyLyrics } from '../src/data/dailyLyrics.js'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const schemaFile = path.join(
  __dirname,
  '..',
  'supabase',
  'migrations',
  '20250526000010_daily_puzzle_tables.sql'
)
const outFile = path.join(
  __dirname,
  '..',
  'supabase',
  'migrations',
  '20250526000011_daily_puzzle_seed.sql'
)
const combinedFile = path.join(
  __dirname,
  '..',
  'supabase',
  'seed',
  'daily_puzzles_complete.sql'
)

function sqlLiteral(value) {
  return `'${String(value).replace(/'/g, "''")}'`
}

function jsonb(value) {
  return `${sqlLiteral(JSON.stringify(value))}::jsonb`
}

function songRows() {
  return dailySongs.map((row) => {
    const slug = row.id
    return `  (${sqlLiteral(slug)}, ${jsonb(row.answers)}, ${jsonb(row.reveals)}, true)`
  })
}

function memberRows() {
  return dailyMembers.map((row) => {
    const slug = row.id.replace(/^m-/, '').replace(/^mv-/, '').replace(/^qt-/, '').replace(/^sr-/, '').replace(/^un-/, '').replace(/^er-/, '').replace(/^tr-/, '') || row.id
    return `  (${sqlLiteral(slug)}, ${sqlLiteral(row.questionType || 'trivia')}, ${sqlLiteral(row.prompt || '')}, ${sqlLiteral(row.displayAnswer)}, ${jsonb(row.answers)}, ${jsonb(row.reveals)}, true)`
  })
}

function lyricRows() {
  return dailyLyrics.map((row) => {
    const slug = row.id.replace(/^l-/, '') || row.id
    return `  (${sqlLiteral(slug)}, ${sqlLiteral(row.song)}, ${sqlLiteral(row.displayAnswer)}, ${jsonb(row.answers)}, ${jsonb(row.reveals)}, true)`
  })
}

const header = `-- Auto-generated by scripts/generate-daily-puzzle-seed.mjs
-- Includes table DDL + seed data (safe to run in one go in Supabase SQL Editor).
-- Re-run npm run generate:daily-seed after editing src/data/daily*.js
--
-- Counts: ${dailySongs.length} songs, ${dailyMembers.length} members, ${dailyLyrics.length} lyrics

`

const songsBlock = `-- ── Daily Song Guess (${dailySongs.length} puzzles) ──
INSERT INTO skz_daily_songs (slug, answers, reveals, is_active) VALUES
${songRows().join(',\n')}
ON CONFLICT (slug) DO UPDATE SET
  answers = EXCLUDED.answers,
  reveals = EXCLUDED.reveals,
  is_active = EXCLUDED.is_active,
  updated_at = now();

`

const membersBlock = `-- ── Daily Member Guess (${dailyMembers.length} puzzles) ──
INSERT INTO skz_daily_members (slug, question_type, prompt, display_answer, answers, reveals, is_active) VALUES
${memberRows().join(',\n')}
ON CONFLICT (slug) DO UPDATE SET
  question_type = EXCLUDED.question_type,
  prompt = EXCLUDED.prompt,
  display_answer = EXCLUDED.display_answer,
  answers = EXCLUDED.answers,
  reveals = EXCLUDED.reveals,
  is_active = EXCLUDED.is_active,
  updated_at = now();

`

const lyricsBlock = `-- ── Daily Lyric Guess (${dailyLyrics.length} puzzles) ──
INSERT INTO skz_daily_lyrics (slug, song, display_answer, answers, reveals, is_active) VALUES
${lyricRows().join(',\n')}
ON CONFLICT (slug) DO UPDATE SET
  song = EXCLUDED.song,
  display_answer = EXCLUDED.display_answer,
  answers = EXCLUDED.answers,
  reveals = EXCLUDED.reveals,
  is_active = EXCLUDED.is_active,
  updated_at = now();
`

const schemaSql = fs.existsSync(schemaFile)
  ? fs.readFileSync(schemaFile, 'utf8') + '\n\n'
  : '-- WARNING: missing 20250526000010_daily_puzzle_tables.sql\n\n'

const seedSql = header + songsBlock + '\n' + membersBlock + '\n' + lyricsBlock + '\n'

fs.writeFileSync(outFile, seedSql)
fs.mkdirSync(path.dirname(combinedFile), { recursive: true })
fs.writeFileSync(combinedFile, schemaSql + seedSql)

console.log(`Wrote ${outFile}`)
console.log(`Wrote ${combinedFile} (tables + seed — use this in SQL Editor)`)
console.log(`  songs: ${dailySongs.length}, members: ${dailyMembers.length}, lyrics: ${dailyLyrics.length}`)
